// Reusable latest-first sorter based on dateAdded
export function productsSortedByDateDesc(list: Product[] = products): Product[] {
  const time = (p: Product) => {
    const t = Date.parse(p.dateAdded);
    return Number.isFinite(t) ? t : 0;
  };
  return [...list].sort((a, b) => time(b) - time(a));
}

// Get products by category
export function getProductsByCategory(category: ProductCategory, list: Product[] = products): Product[] {
  return list.filter(p => p.category === category);
}

// Get products grouped by category
export function getProductsGroupedByCategory(list: Product[] = products): Record<ProductCategory, Product[]> {
  return list.reduce((acc, product) => {
    if (!acc[product.category]) {
      acc[product.category] = [];
    }
    acc[product.category].push(product);
    return acc;
  }, {} as Record<ProductCategory, Product[]>);
}

// Get category info by ID
export function getCategoryInfo(categoryId: ProductCategory) {
  return PRODUCT_CATEGORIES.find(cat => cat.id === categoryId);
}
